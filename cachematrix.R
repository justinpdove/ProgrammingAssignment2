## makeCacheMatrix creates a matrix, solves the inverse of the matrix and assigns 
## a list of functions to variables
## These will be called upon to solve the inverse of a matrix in another function

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinverse <- function(solve) m <<- solve
  getinverse <- function() m
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}


## cacheSolve calls variables in makeCacheMatrix and determines if the matrix has been solved
## If so, it returns the value from getInverse
## If not, solves it and returns the solved matrix

cacheSolve <- function(x, ...) {
  ## Return a matrix that is the inverse of 'x'
  m <- x$getinverse()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinverse(m)
  m
}
